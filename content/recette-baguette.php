<h1>Pain de mie au lait</h1>

<h2>Ingrédients</h2>
<ul class="mx-3 my-1">
    <li>400 g de farine</li>
    <li>250 g d'eau</li>
    <li>7 g de sel</li>
    <li>10 g de levure fraîche</li>
</ul>

<h2>Préparation</h2>
<p>Contrairement au <a href="./recette-pain-lait">pain de mie au lait</a>, je n'hydrate pas la levure.</p>
<p>Mélanger tous les ingrédients.</p>
<p>La pâte pétrie est beaucoup plus liquide que les autres pains.</p>
<p>Je laisse reposer 20 min environ. Je replie la pate sur elle même.</p>
<p>Je laisse reposer à nouveau 20 min environ. Je replie la pate sur elle même.</p>
<p>Je fasconne les baguettes dans le moule à baguette. J'inscire avec un couteau aiguisé.</p>
<p>Laisser reposer 1 à 2h. Le mieux est 2h pour que ça gonfle bien.</p>
