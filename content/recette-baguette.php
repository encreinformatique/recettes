<h1>Baguette</h1>
<p>Dans les <a href="./#pains">recettes de pains</a>, c'est une de mes recettes préférées avec la brioche, plus que le
    <a href="./recette-pain-lait">pain de mie lactal</a>.

<h2>Ingrédients</h2>
<p>Mon moule à baguette est petit car il doit pouvoir rentrer dans le four. Il se pose sur la grille. De toute façon, ce sont les proportions
    qui sont importantes.</p>
<ul class="mx-3 my-1">
    <li>400 g de farine</li>
    <li>250 g d'eau</li>
    <li>7 g de sel</li>
    <li>10 g de levure fraîche</li>
</ul>

<h2>Préparation</h2>
<p>Contrairement au <a href="./recette-pain-lait">pain de mie au lait</a>, je n'hydrate pas la levure.</p>
<p>Mélanger tous les ingrédients.</p>
<p>La pâte pétrie est beaucoup plus liquide que les autres pains.</p>
<p>Je laisse reposer 20 min environ. Je replie la pate sur elle même.</p>
<p>Je laisse reposer à nouveau 20 min environ. Je replie la pate sur elle même.</p>
<p>Je fasconne les baguettes dans le moule à baguette. J'inscire avec un couteau aiguisé.</p>
<p>Laisser reposer 1 à 2h. Le mieux est 2h pour que ça gonfle bien.</p>

<h2>Note pour la cuisson</h2>
<p>Idéalement, le four doit être bien chaud, environ 200 degré, difficile comme le four à gaz que j'ai. Note personnelle : changer cette
    saleté de four ASAP !!</p>