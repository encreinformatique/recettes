<h1 class="text-3xl font-bold mx-3 my-3">Pain de mie au lait</h1>

<h2 class="mx-3">Ingrédients</h2>
<ul class="mx-3 my-1">
    <li>500 g de farine</li>
    <li>200 ml de lait</li>
    <li>20 g de beurre</li>
    <li>40 g de sucre</li>
    <li>1 cuillière à café de sel</li>
</ul>

<p class="mx-3">Pour hydrater la levure</p>
<ul class="mx-3">
    <li>50 ml d'eau (pour hydrater la levure)</li>
    <li>25 g de levure fraîche</li>
</ul>

<h2 class="mx-3">Préparation</h2>
<p class="mx-3">Hydrater la levure.</p>
<p class="mx-3">Mélanger les ingrédients sauf le sel.</p>
