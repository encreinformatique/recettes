<h1>Pain de mie au lait</h1>

<h2>Ingrédients</h2>
<ul class="mx-3 my-1">
    <li>500 g de farine</li>
    <li>200 ml de lait</li>
    <li>20 g de beurre</li>
    <li>40 g de sucre</li>
    <li>1 cuillière à café de sel</li>
</ul>

<p>Pour hydrater la levure</p>
<ul class="mx-3">
    <li>50 ml d'eau (pour hydrater la levure)</li>
    <li>25 g de levure fraîche</li>
</ul>

<h2>Préparation</h2>
<p>Hydrater la levure.</p>
<p>Mélanger les ingrédients sauf le sel.</p>
<p>Ajouter la levure.</p>
<p>Ajouter le sel après avoir mélanger la levure, ou ajouter le sel au tout début et bien mélanger<sup>1<sup>.</p>

<p><sup>1</sup>  Le but est de ne pas tuer la levure. Je trouve que le pain gonfle moins facilement.</p>